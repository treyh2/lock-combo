// Declaring a variable called message and assigning it to a string.

let message = "You have received this message because you have been chosen to open an important vault. Here is the secret combination:"

// Declaring 3 variables: code1, code2, & code3 then assign their values using operators.

let code1 = 11-1;
let code2 = 4*10;
let code3 = 35+4;

// Declare variable combination and assign the values of code1-3 with dashes to separate the codes.
let combination = code1 + "-" + code2 + "-" + code3;

// Create alert() function to display the message and the combination in an alert box.
alert(message + "\n" + combination);